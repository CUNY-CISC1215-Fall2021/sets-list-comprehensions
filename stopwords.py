# Task: Retrieve a list of words from Jane Eyre with stopwords removed

import time

infile = open('jane_eyre.txt')

words = []
num_removed = 0

# Sets have much faster lookups than lists!
# Try commenting out this line (so the stopwords variable remains a list)
# and see how much longer this program takes to run!
stopwords = set(stopwords)

# Time stopword removal
start = time.time()

for line in infile:
    line = line.strip()
    line_words = line.split(' ')

    for word in line_words:
        # Skip stopwords
        if word not in stopwords:
            words.append(word)
        else:
            num_removed += 1

print("Time to remove all stopwords: " + str(time.time() - start))